                        <h2>Rotary’s <span>international</span> growth</h2>
                        
                        
                    </div>
                    <p style="text-align: justify">
                        <br><br>

                        During the 1911-1912 Rotary year, the association became international with the founding of a club in Winnipeg, Manitoba, Canada. Soon Rotary crossed the Atlantic to establish clubs in England, Ireland and Northern Ireland. The National Association of Rotary Clubs, which became the International Association of Rotary Clubs in 1912, adopted the name Rotary International (RI) in 1922.<br><br>
                        
                        Before reaching its 20th birthday, the Rotary association had grown to include some 200 clubs with more than 20,000 members across the globe:
                        <br><br>
                         •   The first Rotary club in Latin America was organised in Havana, Cuba in 1915.<br><br>
                        •   Asia’s first club was established in Manila, Philippines in 1919.<br><br>
                     •   In 1921, Rotary clubs were organised for the first time on continental Europe (Madrid, Spain), Africa (Johannesburg, South Africa), and Australia (Melbourne).</p>
                        <br>
                        <br><br>
                        <h2><span>Rotary</span> today </h2>
                        <br>
                        <br>
                        <p style="text-align: justify">
                        Rotary’s most ambitious undertaking, announced in 1985, is the PolioPlus program — a massive campaign to eradicate polio by the year 2005. Conducted with the cooperation of national governments and intergovernmental agencies such as the World Health Organisation (WHO) and the United Nations Children’s Fund (UNICEF), PolioPlus is a paradigm for public/private sector collaboration in the fight against disease. PolioPlus helps support national and regional polio eradication programs by providing vaccine, surveillance support and social mobilization. By the year 2005 – the target date for certification of a polio-free world – Rotarian contributions to the global polio eradication effort will reach a half billion US dollars.</p>
                        <br>
                        <br>
                        <h2><span>Working for peace</span> – The Rotary Foundation</h2>
                     <br>
                     <br>
                     <p style="text-align: justify">
                     As Rotary grew, so did its scope of activities. During World War I, Rotary discovered new outlets for service — in war relief and peace fund drives at home and in emergency efforts abroad. In 1917, outgoing RI President Arch Klumph proposed the establishment of an endowment fund, which in 1928 became The Rotary Foundation. The Foundation awarded its first humanitarian grant (US$500) in 1930 to the International Society for Crippled Children. After World War II, many clubs that had been disbanded during the conflict were re-established and initiated new service projects, including relief efforts for refugees and prisoners of war.</p>
                     <br><br>
                     <p style="text-align: justify">
                        In the aftermath of World War II, Rotary International sent the largest non-governmental organisation delegation to the United Nations Charter Conference, held in 1945 in San Francisco. Forty-nine Rotarians served as delegates, advisors and consultants to the conference. A Rotary-sponsored conference of education ministers and observers held in London in 1943 was the inspiration for the United Nations Educational, Scientific and Cultural Organisation (UNESCO), established in 1946.</p>
                        <br><br><p style="text-align: justify">
                        The Rotary Foundation enjoyed modest growth until 1947, when Rotarians made a significant number of contributions in memory of Paul Harris, who died in January 1947. That same year the Foundation launched its first program, Graduate Fellowships (today called Ambassadorial Scholarships), sending 18 students abroad to seven countries. Today, approximately 1,300 students study abroad as Rotary Scholars every year.</p>
                        <br><br>
                        <p style="text-align: justify">
                        Two of Rotary’s programs for young people, Rotaract and Interact, were started during the turbulent 1960s. Interact (for youth ages 14-18) and Rotaract (for young adults ages 18-30) clubs operate under the guidance of a sponsoring Rotary club and give young people opportunities for community service and leadership development, and to promote international peace and understanding. Service to youth remains an important focus of Rotary.</p><br>
                        <br>
                        <br>
                        <h2>Women in <span>Rotary</span></h2>
                        <br>
                        <br>
                        <p style="text-align: justify">
                        First admitted in 1987, women are today the fastest-growing segment of Rotary membership, and increasingly hold leadership positions within the organisation. More than 2,000 women serve as club presidents and women are also rapidly assuming regional leadership roles. Currently, some 1.2 million professional men and women belong to more than 30,000clubs worldwide.</p>
                        <br><br>
                        <p style="text-align: justify">
                        Rotary continues to grow internationally. After the collapse of the Iron Curtain, Rotary clubs which had been disbanded during World War II were re-established in central and eastern Europe. In 1990, Rotary clubs were formed in Russia for the first time, and other former Soviet republics soon followed. Kyrgyzstan, once a part of the Soviet Union, is a recent addition.</p>
                        <br><br>
                        <p style="text-align: justify">
                        Today, Rotary International encourages its clubs to focus on a broad spectrum of service activities such as hunger, the environment, violence prevention, illiteracy, drug abuse prevention, polio eradication, youth, the elderly, and AIDS awareness and education. Rotary clubs around the world are united under the motto “Service Above Self.”</p>
                        <br>
                        <br><br>
                        <h2>Ever wonder why the <span>Rotary<span> year begins<span> 1 July<span>?</h2>
                        <br>
                        <br>
                        <p style="text-align: justify">
                        The international convention initially played a key role in determining the start date of our fiscaland administrative year.<br><br>
                        Rotary’s first fiscal year began the day after the first convention ended, on 18 August 1910. The 1911-12 fiscal year also related to the convention, beginning with the first day of the 1911 convention on 21 August.<br><br>
                        At its August 1912 meeting, the Board of Directors ordered an audit of the International Association of Rotary Clubs’ finances. The auditors recommended that the organization end its fiscal year on 30 June to give the secretary and treasurer time to prepare a financial statement for the convention and board, and determine the proper number of club delegates to the convention.
                        The executive committee concurred, and at its April 1913 meeting, designated 30 June as the end of the fiscal year. This also allowed for changes to the schedule for reporting club membership and payments. Even The Rotarian changed its volume numbering system to correspond to the fiscal year (beginning with vol. 5, July 1914).<br><br>
                        Rotary continued to hold its annual conventions in July or August until 1917. Delegates to the 1916 event in Cincinnati, Ohio, USA, approved a resolution to hold future conventions in June, mainly because of the heat in cities where most of them occurred. The next one was held 17-21 June in Atlanta, Georgia.<br><br>
                        The term “Rotary year” has been used to signify Rotary’s annual administrative period since at least 1913. An article in The Rotarian that July noted, “The Rotary year that is rapidly drawing to a close has been signalized by several highly successful joint meetings of Clubs that are so situated as to assemble together easily and conveniently.”<br><br>
                        Since the executive committee’s decision in 1913, the end of the Rotary year has remained 30 June.



