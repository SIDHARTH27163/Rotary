                      <p style="text-align: justify;">
                        Harris eventually persuaded several business associates to discuss the idea of forming an organization for local professionals. On 23 February 1905, Harris, Gustavus Loehr, Silvester Schiele, and Hiram Shorey gathered at Loehr’s office in downtown Chicago for what would become known as the first Rotary club meeting.<br>
                        In February 1907, Harris was elected the third president of the Rotary Club of Chicago. Toward the end of his presidency, he worked to expand Rotary beyond the city. Some club members resisted, not wanting to take on the additional financial burden. But Harris persisted, and by 1910, Rotary had expanded to several other major U.S. cities.<br>
                        Harris recognized the need to form a national association with an executive board of directors. In August 1910, Rotarians held their first national convention in Chicago, where the 16 existing clubs unified as the National Association of Rotary Clubs (now Rotary International). The new association unanimously elected Harris as its president.<br>
                        At the end of his second term as Rotary president, Harris resigned, citing ill health and the demands of his professional practice and personal life. He was elected president emeritus by convention action, a title he held until his death.<br>
                         In  the mid-1920s, Harris became actively involved in Rotary again, serving as the public face of the organization. To promote membership and service, he attended conventions and visited clubs throughout the world, often accompanied by his wife, Jean.<br>
                        Harris died on 27 January 1947 in Chicago at age 78, after a prolonged illness. Before his death, he made it known that he preferred contributions to The Rotary Foundation in lieu of flowers. By coincidence, days before he died, Rotary leaders had committed to a major fundraising effort for the Foundation. <br>
                        Upon  news of his death, Rotary created the Paul Harris Memorial Fund as a way to solicit these donations. Rotarians were encouraged to commemorate the late founder of Rotary by contributing to the fund, which would be used for purposes dear to Harris’ heart. In the 18 months following his death, The Rotary Foundation received $1.3 million, which helped support the Foundation’s first program — scholarships for graduate study abroad


