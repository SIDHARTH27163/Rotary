                      <p style="text-align: justify;">
                        Before he died, Rotary founder Paul Harris requested that memorial donations, in lieu of flowers, be made to The Rotary Foundation to support education. But in his youth, Harris was a mischievous child and an itinerant student. 

                        <br><br> Harris was born in 1868 in Racine, Wisconsin. Three years later, as the family struggled financially, Paul and  his older brother, Cecil, were sent to live with their paternal grandparents in Wallingford, Vermont, in 1871, where Harris attended primary school in a single-room schoolhouse that today serves as the meeting place of the Rotary Club of Wallingford. He was prone to playing pranks and hooky, later attending secondary school in Rutland, Vermont. He was expelled from Black River Academy in Ludlow after only a few weeks.

                        <br><br>“The thought persisted that I was experiencing only what had happened to hundreds, perhaps thousands, of others in the great city. ... I was sure that there must be many other young men who had come from farms and small villages to establish themselves in Chicago. ... Why not bring them together? If others were longing for fellowship as I was, something would come of it.”

                        <br><br>After attending the Vermont Military Academy, Harris enrolled at the University of Vermont in Burlington in 1885. He tried out for the freshman football team and earned a spot on it. In December 1886, he and three others were expelled for their conduct as members of an underground society. He later wrote that although he was innocent of the particular act that he was accused of, the expulsion was nonetheless justified.

