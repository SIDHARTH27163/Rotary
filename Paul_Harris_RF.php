                    <p style="text-align: justify;">
                        Harris  was born on 19 April 1868 in Racine, Wisconsin, USA. At age 3, he moved to Wallingford, Vermont, where he grew up in the care of his paternal grandparents. He attended the University of Vermont and Princeton University and received his law degree from the University of Iowa in 1891.

                        <br><br> In 1896, Harris settled in Chicago and opened a law practice. Four years later, he met fellow attorney Bob Frank for dinner on Chicago’s North Side. They walked around the area, stopping at shops along the way. Harris was impressed that Frank was friendly with many of the shopkeepers. He had not seen this kind of camaraderie among businessmen since moving to Chicago and wondered if there was a way to channel it, because it reminded him of growing up in Wallingford. 

                        <br><br>“The thought persisted that I was experiencing only what had happened to hundreds, perhaps thousands, of others in the great city. ... I was sure that there must be many other young men who had come from farms and small villages to establish themselves in Chicago. ... Why not bring them together? If others were longing for fellowship as I was, something would come of it.”

